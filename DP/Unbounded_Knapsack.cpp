using namespace std;
#define ll long long 
#define endl '\n' 

ll dp[202][100002];
ll mem_uknap(vector<ll>&w,vector<ll>&v,ll n, ll k)
{
	if(dp[n][k]!=-1) return dp[n][k];
	if(k==0||n==0) return dp[n][k] = 0;
	if(w[n-1]<=k)
		return dp[n][k] = max(v[n-1]+mem_uknap(w,v,n,k-w[n-1]),mem_uknap(w,v,n-1,k));
	return dp[n][k] = mem_uknap(w,v,n-1,k);
}
ll bu_uknap(vector<ll>&w,vector<ll>&v,ll k)
{
	ll n=v.size();
	ll dp[n+1][k+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 0;
	for(ll i=0;i<=k;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=k;++j)
		{
			if(w[i-1]<=j)
				dp[i][j] = max(v[i-1]+dp[i][j-w[i-1]] , dp[i-1][j] );
			else
				dp[i][j] = dp[i-1][j];
		}
	}
	return dp[n][k];
}
