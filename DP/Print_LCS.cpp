#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 

string bu_lcs(string s, string t)
{
	ll n=s.size();
	ll m=t.size();
	ll dp[n+1][m+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 0;
	for(ll i=0;i<=m;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=m;++j)
		{
			if(s[i-1]==t[j-1])
				dp[i][j] = 1 + dp[i-1][j-1];
			else
				dp[i][j] = max(dp[i-1][j],dp[i][j-1]);
		}
	}
	ll i=n,j=m;
	string ans;
	while(i>0&&j>0)
	{
		if(s[i-1]==t[j-1])
		{
			ans.push_back(s[i-1]);
			i--;
			j--;
		}
		else if(dp[i-1][j]>dp[i][j-1])
		{
			i--;
		}
		else
		{
			j--;
		}
	}
	reverse(ans.begin(),ans.end());
	return ans;
}
