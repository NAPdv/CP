ll dp[202][100002];
ll mem_rc(vector<ll>&p,vector<ll>&l,ll r,ll n)
{
	if(dp[n][r]!=-1) return dp[n][r];
	if(r==0||n==0) return 0;
	if(l[n-1]<=r)
		return dp[n][r] = max(p[n-1]+mem_rc(p,l,r-l[n-1],n),mem_rc(p,l,r,n-1));
	else
		return dp[n][r] = mem_rc(p,l,r,n-1);

}
ll bu_rc(vector<ll>&p,vector<ll>&l,ll r)
{
	ll n=p.size();
	ll dp[n+1][r+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 0;
	for(ll i=0;i<=r;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=r;++j)
		{
			if(l[i-1]<=j)
			{
				dp[i][j] = max(p[i-1]+dp[i][j-l[i-1]],dp[i-1][j]);
			}
			else
				dp[i][j] = dp[i-1][j];
		}
	}
	return dp[n][r];
}
