#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
ll mem[1002][1002];
ll bu_og(vector<ll>&a)
{
	ll n=a.size();
	ll dp[n][n];
	for(ll i=0;i<n-1;++i) dp[i][i+1] = max(a[i],a[i+1]);
		for(ll gap=3;gap<n;gap+=2)
			for(ll i=0;i+gap<n;++i)
			{
				ll j=i+gap;
				dp[i][j]=max(a[i]+min(dp[i+1][j-1],dp[i+2][j]),a[j]+min(dp[i][j-2],dp[i+1][j-1]));
			}
	return dp[0][n-1];
}
ll rec_og(vector<ll>&a,ll i,ll j)
{
	if(mem[i][j]!=-1) return mem[i][j];
	if(i+1==j) return mem[i][j] = max(a[i],a[j]);
	return mem[i][j] = max( a[i] + min( rec_og(a,i+1,j-1) , rec_og(a,i+2,j)),a[j] + min(rec_og(a,i,j-2) , rec_og(a,i+1,j-1)));
}
