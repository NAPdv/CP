{
	ll t=1;
	//cint>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		vector<ll>a(n);
		for(ll i=0;i<n;++i)
			cin>>a[i];
		cout<<bu_og(a)<<endl;
		memset(mem,-1,sizeof(mem));
		cout<<rec_og(a,0,a.size()-1)<<endl;
	}
}