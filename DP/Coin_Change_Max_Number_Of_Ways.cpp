#define ll long long 
#define endl '\n' 

ll dp[202][100002];

ll mem_cc(vector<ll>&c,ll k,ll n)
{
	if(dp[n][k]!=-1) return dp[n][k];
	if(k==0) return dp[n][k] = 1;
	if(n==0) return dp[n][k] = 0;
	if(c[n-1]<=k)
		return dp[n][k] = mem_cc(c,k-c[n-1],n) + mem_cc(c,k,n-1);
	return dp[n][k] = mem_cc(c,k,n-1);
}
ll bu_cc(vector<ll>&c,ll k)
{
	ll n=c.size();
	ll dp[n+1][k+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 1;
	for(ll i=1;i<=k;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=k;++j)
		{
			dp[i][j] = dp[i-1][j];
			if(c[i-1]<=j)
			{
				dp[i][j]+=dp[i][j-c[i-1]];
			}
		}
	}
	return dp[n][k];
}
