//Binary Search
ll bs_lis(vector<ll>&a)
{
	ll n=a.size();
	if(n==0) return 0;
	vector<ll>lis;
	lis.push_back(a[0]);
	for(ll i=1;i<n;++i)
	{
		if(a[i]>lis[lis.size()-1])
		{
			lis.push_back(a[i]);
		}
		else
		{
			ll index = ub(lis,a[i]);
			lis[index] = a[i];
		}
	}
	return lis.size();

}
