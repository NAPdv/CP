ll bu_lis(vector<ll>&a)
{
	ll n=a.size();
	if(n==0) return 0;
	vector<ll>lis(n,1);
	for(ll i=0;i<n;++i)
	{
		for(ll j=i-1;j>=0;--j)
		{
			if(a[i]>a[j])
			{
				lis[i] = max(lis[i],lis[j]+1);
			}
		}
	}
	ll m=1;
	for(ll i=0;i<n;++i)
	{
		m=max(m,lis[i]);
	}
	return m;
}
ll ub(vector<ll>&a, ll x)
{
	ll l=0;
	ll r=a.size()-1;
	ll res=0;
	while(l<=r)
	{
		ll m=l+(r-l)/2;
		if(a[m]==x)
			return m;
		else if(a[m]>x)
			res=m,r=m-1;
		else
			l=m+1;
	}
	return res;
}
