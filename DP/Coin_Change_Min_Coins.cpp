ll dp[202][100002];

ll mem_cc(vector<ll>&a,ll k,ll n)
{
	
	if(k==0) return 0;
	if(n==0) return INT_MAX-1;
	// if(k>=1&&n==1) {
	// 	if(k%a[n]==0) return k/a[n];
	// 	else return  INT_MAX-1;
	// }
	if(dp[n][k]!=-1) return dp[n][k];
	if(a[n-1]<=k)
		return dp[n][k] = min( 1 + mem_cc(a,k-a[n-1],n) , mem_cc(a,k,n-1));
	return dp[n][k] = mem_cc(a,k,n-1);
}
ll bu_cc(vector<ll>&a,ll k)
{
	ll n=a.size();
	ll dp[n+1][k+1];
	for(ll i=0;i<=k;++i) dp[0][i] = INT_MAX-1;
	for(ll i=0;i<=n;++i) dp[i][0] = 0;
	// for(ll i=1;i<=k;++i)
	// {
	// 	if(i%a[0]==0) dp[1][i] = i/a[0];
	// 	else dp[1][i] = INT_MAX-1;
	// }
	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=k;++j)
		{
			if(a[i-1]<=j)
				dp[i][j] = min( 1 + dp[i][j-a[i-1]] , dp[i-1][j]);
			else
				dp[i][j] = dp[i-1][j];
		}
	}
	for(ll i=0;i<=n;++i)
	{
		for(ll j=0;j<=k;++j)
		{
			cout<<dp[i][j]<<" ";
		}
		cout<<endl;
	}
	return dp[n][k];
}
