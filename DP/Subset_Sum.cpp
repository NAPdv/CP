    // }
    bool dp[n+1][k+1];
    for(ll i=0;i<=n;++i) dp[i][0] = 1;
    for(ll i=1;i<=k;++i) dp[0][i] = 0;

    for(ll i=1;i<=n;++i)
    {
        for(ll j=1;j<=k;++j)
        {
            //dp[i][j] = dp[i-1][j];
            if(a[i-1]<=j)
                dp[i][j] = dp[i-1][j-a[i-1]] || dp[i-1][j];
            else
                dp[i][j] = dp[i-1][j];
        }
    }
    // for(ll i=0;i<=n;++i)
    // {
