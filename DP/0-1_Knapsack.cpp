ll dp[1001][1001];
ll rec_knap(vector<ll>&w,vector<ll>&v,ll c,ll n){
    if(n==0||c==0)
        return 0;
    if(w[n-1]<=c)
    {
        return max(v[n-1]+rec_knap(w,v,c-w[n-1],n-1),rec_knap(w,v,c,n-1));
    }
    return rec_knap(w,v,c,n-1);
}
ll mem_knap(vector<ll>&w,vector<ll>&v,ll c,ll n)
{

    if(n==0||c==0)
        return 0;
    if(dp[n][c]!=-1) return dp[n][c];
    if(w[n-1]<=c)
    {
        return dp[n][c] = max(v[n-1]+rec_knap(w,v,c-w[n-1],n-1),rec_knap(w,v,c,n-1));
    }
    return dp[n][c] = rec_knap(w,v,c,n-1);

}
ll td_knap(vector<ll>&w,vector<ll>&v,ll c,ll n)
{
    ll dp[n+1][c+1];
    for(ll i=0;i<=n;++i) dp[i][0] = 0;
    for(ll i=0;i<=c;++i) dp[0][i] = 0;

    for(ll i=1;i<=n;++i)
    {
        for(ll j=1;j<=c;++j)
        {
            if(w[i-1]<=j)
                dp[i][j]=max(v[i-1]+dp[i-1][j-w[i-1]],dp[i-1][j]);
            else
                dp[i][j]=dp[i-1][j];
        }
    }
    return dp[n][c];

}
