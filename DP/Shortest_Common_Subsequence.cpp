ll dp[202][100002];

ll mem_scs(string s,string t,ll n,ll m)
{
  if(dp[n][m]!=-1) return dp[n][m];
  if(n==0||m==0) return dp[n][m] = 0;
  if(s[n-1] == t[m-1])
    return dp[n][m] =1 + mem_scs(s,t,n-1,m-1);
  return dp[n][m] = max(mem_scs(s,t,n-1,m) ,mem_scs(s,t,n,m-1));
}
ll bu_scs(string s,string t)
{
  ll n=s.size();
  ll m=t.size();
  ll dp[n+1][m+1];
  for(ll i=0;i<=n;++i) dp[i][0] = 0;
  for(ll i=0;i<=m;++i) dp[0][i] = 0;

  for(ll i=1;i<=n;++i)
  {
    for(ll j=1;j<=m;++j)
    {
      if(s[i-1] == t[j-1])
        dp[i][j] = 1 + dp[i-1][j-1];
      else
        dp[i][j] = max(dp[i-1][j],dp[i][j-1]);
    }
  }
  return s.size()+t.size()-dp[n][m];
}
