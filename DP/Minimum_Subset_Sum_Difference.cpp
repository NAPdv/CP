#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 

ll td_mss(vector<ll>&a)
{
	ll n=a.size();
	ll s=0;
	for(ll i=0;i<n;++i)
	{
		s+=a[i];
	}
	bool dp[n+1][s/2+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 1;
	for(ll i=1;i<=s/2;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=s/2;++j)
		{
			dp[i][j] = dp[i-1][j];
			if(a[i-1]<=j)
				dp[i][j] = dp[i][j] || dp[i-1][j-a[i-1]];
		}
	}
	ll c=0;
	for(ll i=s/2;i>=0;--i)
	{
		if(dp[n][i])
		{
			c=i;
			break;
		}
	}
	return s-2*c;
}
