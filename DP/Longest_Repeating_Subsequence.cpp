using namespace std;
#define ll long long 
#define endl '\n' 
ll mem[2005][20002];
ll mem_lrs(string s,ll n,ll m)
{
	if(n==0||m==0)
		return 0;
	if(mem[n][m]!=-1) return mem[n][m];
	if(s[n-1]==s[m-1]&&n!=m)
		return mem[n][m] = 1 + mem_lrs(s,n-1,m-1);
	else
		return mem[n][m] = max( mem_lrs(s,n-1,m) , mem_lrs(s,n,m-1));
}
ll bu_lrs(string s)
{
	ll n=s.size();
	ll dp[n+1][n+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 0, dp[0][i] = 0;
	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=n;++j)
		{
			if(s[i-1] == s[j-1] && i!=j)
			{
				dp[i][j] = 1 + dp[i-1][j-1];
			}
			else
			{
				dp[i][j] = max(dp[i-1][j],dp[i][j-1]);
			}
		}
	}
	return dp[n][n];
}
