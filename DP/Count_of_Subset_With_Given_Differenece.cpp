ll dp[202][10002];
ll mem_css(vector<ll>&a,ll n, ll s)
{
	if(dp[n][s]!=-1) return dp[n][s];
	if(s==0) return dp[n][s] = 1;
	if(n==0) return dp[n][s] = 0;
	if(a[n-1]<=s)
		return dp[n][s] = mem_css(a,n-1,s-a[n-1]) + mem_css(a,n-1,s);
	return dp[n][s] = mem_css(a,n-1,s);
}
ll bu_css(vector<ll>&a,ll s)
{
	ll n=a.size();
	ll dp[n+1][s+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 1; 
	for(ll i=1;i<=s;++i) dp[0][i] = 0;
	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=s;++j)
		{
			dp[i][j] = dp[i-1][j];
			if(a[i-1]<=j)
				dp[i][j] += dp[i-1][j-a[i-1]];
		}
	}
	return dp[n][s];
}
