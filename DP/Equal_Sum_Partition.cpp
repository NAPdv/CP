ll dp[102][100002];
ll mem_esp(vector<ll>&a,ll s,ll n)
{
	if(s==0) return true;
	if(n==0) return false;
	if(dp[n][s]!=-1) return dp[n][s];
	if(a[n-1]<=s)
		return dp[n][s] = mem_esp(a,s-a[n-1],n-1) || mem_esp(a,s,n-1);
	return dp[n][s] = mem_esp(a,s,n-1);
}
bool td_esp(vector<ll>&a)
{
	ll n=a.size();
	ll s=0;
	for(ll i=0;i<n;++i)
	{
		s+=a[i];
	}
	if(s&1) return false;
	bool dp[n+1][s/2+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 1;
	for(ll i=0;i<=s/2;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=s/2;++j)
		{
			dp[i][j]=dp[i-1][j];
			if(a[i-1]<=j)
				dp[i][j] = dp[i][j] || dp[i-1][j-a[i-1]];
		}
	}
	return dp[n][s/2];
}
