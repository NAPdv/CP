#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
int main()
{
	ll t=1;
	cin>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		vector<ll>a(n);
		for(ll i=0;i<n;++i)
		{
			cin>>a[i];
		}
		if(a[0]==-1) a[0]=0;
		ll s=a[0];
		for(ll i=1;i<n;++i)
		{
			
			if(a[i]==-1)
			{
				a[i]=(s)/i;
			}
			s+=a[i];
		}
		for(ll i=0;i<n;++i)
		{
			cout<<a[i]<<" ";
		}
		cout<<endl;
	}
}
