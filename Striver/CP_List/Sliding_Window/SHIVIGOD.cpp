#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 

int main()
{
	
	ll t=1;
	cin>>t;
	while(t--)
	{
		ll n,b,a;
		cin>>n>>b>>a;
		vector<ll>ar(n);
		for(ll i=0;i<n;++i)
		{
			cin>>ar[i];
		}
		double mx=INT_MIN;
		for(ll i=a;i<=b;++i)
		{
			double s=0;
			for(ll j=0;j<i;++j)
			{
				s+=ar[j];
			}				
			mx=max(mx,(s*1.0)/(i*1.0));
			for(ll j=i;j<n;++j)
			{
				s=s-ar[j-i]+ar[j];
				mx=max(mx,(s*1.0)/(i*1.0));
			}
		}
		cout<<fixed<<setprecision(6)<<mx<<endl;
	}

}
