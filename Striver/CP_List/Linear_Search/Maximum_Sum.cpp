#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		vector<ll>a(n);
		ll c=0,s=0;
		ll mx=INT_MIN;
		for(ll i=0;i<n;++i)
		{
			cin>>a[i];
			if(a[i]>=0)
			{
				s+=a[i];
				c++;
				
			}
			mx=max(mx,a[i]);
		}
		if(c==0)
		{
			cout<<mx<<" "<<1<<endl;
			return 0;
		}
		cout<<s<<" "<<c<<endl;
	}
	
}
