{
	
	ll t=1;
	cin>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		if(check(n)||n%21==0)
		{
			cout<<"The streak is broken!"<<endl;
		}
		else{
			cout<<"The streak lives still in our heart!"<<endl;
		}
	}

}
