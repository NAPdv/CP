#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		string s;
		getline(cin,s);
		map<char,ll>m;
		char c='a';
		ll mx=0;
		for(ll i=0;i<s.size();++i)
		{
			m[s[i]]++;
			if(mx<m[s[i]])
			{
				c=s[i];
				mx=m[s[i]];
			}
			if(mx==m[s[i]]&&s[i]<c)
			{
				c=s[i];
			}
		}
		cout<<c<<" "<<mx<<endl;
	}
}
