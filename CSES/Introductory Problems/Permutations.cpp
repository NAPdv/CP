vector<ll> ff(ll n)
{
	vector<ll>v;
	for(ll i=2;i<=n;i+=2)
	{
		v.emplace_back(i);
	}
	for(ll i=1;i<=n;i+=2)
	{
		v.emplace_back(i);
	}
	return v;
}
