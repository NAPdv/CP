using namespace std;
#define ll long long
#define endl '\n'

const ll M=1e9+7;
ll powm(ll x,ll y,ll m)
{
	if(y==0)
		return 1;
	x=x%m;
	if(y&1)
	{
		ll t=powm(x,(y/2),m);
		return (((t*t)%m)*x)%m;
	}
	else
	{
		ll t=powm(x,(y/2),m);
		return (t*t)%m;
	}
}
