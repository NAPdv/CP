#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
 
const ll M = 1e9+7;
ll dp[502][100002];
ll powmr(ll x,ll y,ll m)
{
	if(y==0) return 1;
	if(y%2==0)
	{
		ll t=powmr(x,(y/2),m);
		return (t*t)%m;
	}
	else
	{
		ll t=powmr(x,(y/2),m);
		return ((((t*t)%m)*x)%m);
	}
}
ll mem_css(vector<ll>&a,ll s,ll n)
{
	if(dp[n][s]!=-1) return dp[n][s];
	if(s==0) return dp[n][s] = 1;
	if(n==0) return dp[n][s] = 0;
	if(a[n-1]<=s) 
		return dp[n][s] = (mem_css(a,s-a[n-1],n-1)%M+ mem_css(a,s,n-1)%M)%M;
	return dp[n][s] = mem_css(a,s,n-1)%M;
}
int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		ll n,s=0;
		cin>>n;
		vector<ll>a(n);
		for(ll i=0;i<n;++i)
			a[i] = i+1,s+=a[i];
		memset(dp,-1,sizeof(dp));
		if(s%2==0)
		cout<<(mem_css(a,s/2,n)*powmr(2,M-2,M))%M<<endl;
		else
		cout<<0<<endl;
	}
}
