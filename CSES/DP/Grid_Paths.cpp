#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
const ll M = 1e9+7;


int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		char c[n][n] ;
		for(ll i=0;i<n;++i)
		{
			for(ll j=0;j<n;++j)
			{
				cin>>c[i][j];
			}
		}
		vector<vector<ll>>dp(n,vector<ll>(n,0));
		if(c[n-1][n-1] == '.') dp[n-1][n-1] = 1;
		for(ll i=n-2;i>=0;--i)
		{
			if(c[n-1][i] == '.') 
				dp[n-1][i] = (dp[n-1][i+1])%M;
			else
				dp[n-1][i] = 0;
			if(c[i][n-1] == '.')
				dp[i][n-1] =(dp[i+1][n-1])%M;
			else
				dp[i][n-1] = 0;
		}
		for(ll i=n-2;i>=0;--i)
		{
			for(ll j=n-2;j>=0;--j)
			{
				if(c[i][j]=='.') 
					dp[i][j] = (dp[i][j] + dp[i+1][j] + dp[i][j+1])%M;
				else
					dp[i][j] = 0;
			}
		}
		cout<<dp[0][0]<<endl;
	}
}
