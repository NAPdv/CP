#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
const ll M = 1e9+7;


int main()
{
	ll n,k;
	cin>>n>>k;
	vector<ll>a(n);
	for(ll i=0;i<n;++i)
		cin>>a[i];
	vector<ll> dp(k+1,INT_MAX);
	dp[0]=0;
	for(ll i=1;i<=k;++i)
	{
		for(ll j=0;j<n;++j)
		{
			if(a[j]<=i)
				dp[i] = min(dp[i],1 + dp[i-a[j]]);
		}
	}
	if(dp[k]==INT_MAX)
		cout<<-1<<endl;
	else
		cout<<dp[k]<<endl;
}
