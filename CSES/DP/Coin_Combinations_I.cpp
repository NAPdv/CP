#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
const ll M = 1e9+7;

int main()
{
	ll n,k;
	cin>>n>>k;
	vector<ll>a(n);
	for(ll i=0;i<n;++i)
		cin>>a[i];
	ll dp[k+1] = {0};
	dp[0]=1;
	for(ll i=1;i<=k;++i)
	{
		for(ll j=0;j<n;++j)
		{
			if(a[j]<=i)
				dp[i] = (dp[i] + dp[i-a[j]])%M;
		}
	}
	cout<<dp[k]%M<<endl;
}
