using namespace std;
#define ll long long 
#define endl '\n' 
const ll M = 1e9+7;


int main()
{
	ll n;
	cin>>n;
	vector<ll>dp(n+1,INT_MAX);
	dp[0]=0;
	for(ll i=1;i<=n;++i)
	{
		ll p=i;
		while(p)
		{
			ll x=p%10;
			if(x) dp[i] = min(dp[i] , 1 + dp[i-x]);
			p=p/10;
		}
	}
	cout<<dp[n]<<endl;
}
