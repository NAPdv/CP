#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 

vector<ll> dp_cc(vector<ll>&a)
{
	vector<ll>v;
	ll n=a.size();
	ll s=0;
	for(ll i=0;i<n;++i)
	{
		s+=a[i];
	}
	bool dp[n+1][s+1];
	for(ll i=0;i<=n;++i) dp[i][0] = 1;
	for(ll i=1;i<=s;++i) dp[0][i] = 0;

	for(ll i=1;i<=n;++i)
	{
		for(ll j=1;j<=s;++j)
		{
			dp[i][j] =dp[i-1][j];
			if(a[i-1]<=j)
				dp[i][j] |= dp[i-1][j-a[i-1]];
		}
	}
	for(ll i=1;i<=s;++i)
	{
		if(dp[n][i])
			v.push_back(i);
	}
	return v;
}

int main()
{
	ll t=1;
	//cin>>t;
	while(t--)
	{
		ll n;
		cin>>n;
		vector<ll>a(n);
		for(ll i=0;i<n;++i)
		{
			cin>>a[i];
		}
		vector<ll>v = dp_cc(a);
		cout<<v.size()<<endl;
		for(ll i=0;i<(ll)v.size();++i)
		{
			cout<<v[i]<<" ";
		}
		cout<<endl;
	}
}
