#include<bits/stdc++.h>
using namespace std;
#define ll long long 
#define endl '\n' 
const ll M = 1e9+7;


int main()
{
	ll n,k;
	cin>>n>>k;
	vector<ll>a(n);
	for(ll i=0;i<n;++i)
		cin>>a[i];
	ll dp[k+1]={0};
	dp[0]=1;
	for(ll i=0;i<n;++i)
	{
		for(ll j=1;j<=k;++j)
		{
			if(a[i]<=j)
			{
				dp[j] = (dp[j]+dp[j-a[i]])%M;
			}
		}
	}
	cout<<dp[k]<<endl;
}
