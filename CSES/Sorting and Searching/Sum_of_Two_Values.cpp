#include<bits/stdc++.h>
using namespace std;
#define ll long long
#define endl '\n'

const ll M=1e9+7;
int main()
{
	ll t=1;
	// cin>>t;
	while(t--)
	{
		ll n,k;
		cin>>n>>k;
		vector<ll>a(n);
		map<ll,ll>m;
		for(ll i=0;i<n;++i)
		{
			cin>>a[i];
			m[a[i]]=i;
		}
		bool ok=false;
		pair<ll,ll>p;
		for(ll i=0;i<n-1;++i)
		{
			if(m.find(k-a[i])!=m.end()&&m[k-a[i]]!=i)
			{
				ok=true;
				p={i,m[k-a[i]]};
			}
		}
		if(ok)
		{
			cout<<p.first+1<<" "<<p.second+1<<endl;
		}
		else
		{
			cout<<"IMPOSSIBLE"<<endl;
		}
	}
}